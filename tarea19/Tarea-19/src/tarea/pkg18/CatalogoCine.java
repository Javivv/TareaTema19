    // Boton eliminar masivo
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField4.getText().length() != 0 && (jRadioButton1.isSelected() || jRadioButton2.isSelected() || jRadioButton3.isSelected())){ // Le exigimos que debe haber introducido un año y presionar alguno de los botones
        
                int puntuacion = Integer.parseInt("" + jComboBox2.getSelectedItem());
                int anyo = Integer.parseInt(jTextField4.getText());

                if (jRadioButton1.isSelected()) { // <
                    conexion.ejecutarDeleteMenor(anyo, puntuacion);

                } else if (jRadioButton2.isSelected()) { // =
                    conexion.ejecutarDeleteIgual(anyo, puntuacion);

                } else { // >
                    conexion.ejecutarDeleteMayor(anyo, puntuacion);
                }
                actualizarTabla();
        
            } else {
                String mensaje = "Debe completar todos los campos para eliminar.";
                String titulo = "Error al eliminar";
                JOptionPane.showMessageDialog(this,
                        mensaje,
                        titulo,
                        2);
            }



        } catch (Exception e) {
            // TODO: handle exception
        }
    }//GEN-LAST:event_jButton3ActionPerformed

