    /** En este método añadiremos una nueva película a la base de datos y la actualizaremos
     * 
     */
    private void anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anadirActionPerformed

        try {
            if (jTextField1.getText().length() > 0 && jTextField2.getText().length() > 0 && jTextField3.getText().length() > 0) { // Comprobamos que los campos de texto no se encuentren vacíos
        
                // Almacenamos los valores introducidos en los campos.
                String titulo = jTextField1.getText();
                int anyo = Integer.parseInt(jTextField2.getText());
                String sinopsis = jTextField3.getText();
                int puntuacion = Integer.parseInt(jComboBox1.getSelectedItem() + "");
                
                Pelicula peliculaAAnyadir = new Pelicula(titulo, anyo, puntuacion, sinopsis); // Creamos un objeto pelicula con los valores introducidos por parámetros
                conexion.getBd().store(peliculaAAnyadir); // Almacenamos la película en la base de datos.

        
                limpiarActionPerformed(evt); // Limpiamos los campos del formulario.
                actualizarTabla(); // Actualizamos la tabla con la pelicula nueva.
                
            } else { // En caso de faltar algun campo por rellenar creamos una ventana emergente.
                String mensaje = "Debe rellenar todos los campos del formulario.";
                String titulo = "Error al añadir";
                JOptionPane.showMessageDialog(this,
                mensaje,
                titulo,
                2);
            }
            
        } catch (Exception e) {
            // TODO: handle exception
        }

    }//GEN-LAST:event_anadirActionPerformed


