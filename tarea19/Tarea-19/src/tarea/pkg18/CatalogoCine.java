    public void inicializarMenu(){
        
        // Creamos los diferentes items y les damos nombres
        JMenuItem borrar = new JMenuItem("Borrar");
        JMenuItem editar = new JMenuItem("Editar");
        
        // Generamos los popmenu
        jPopupMenu1.add(borrar);
        jPopupMenu1.add(editar);
        
        jTable1.setComponentPopupMenu(jPopupMenu1); // Añadimos los popmenu como componentes de la tabla
        
        // Le agregamos un icono a cada popmenu
        borrar.setIcon(new ImageIcon("/Users/javivv/Desktop/Escritorio/Estudios/DAW/Primero/Programación/Ejercicios/3ᵃ Evaluación/Tema 17/Tarea17_2/src/main/java/com/mycompany/tarea17_2/logos/delete-icon.png"));
        editar.setIcon(new ImageIcon("/Users/javivv/Desktop/Escritorio/Estudios/DAW/Primero/Programación/Ejercicios/3ᵃ Evaluación/Tema 17/Tarea17_2/src/main/java/com/mycompany/tarea17_2/logos/update-logo.png"));
        

        // Con este método eliminaremos la película seleccionada en el tablemodel con el click derecho.
        borrar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                try {
            
                    // Extraemos los parámetros de la película seleccionada.
                    String titulo = "" + jTable1.getValueAt(jTable1.getSelectedRow(), 0); 
                    int anyo = Integer.parseInt("" + jTable1.getValueAt(jTable1.getSelectedRow(), 1));
                    int puntuacion = Integer.parseInt("" + jTable1.getValueAt(jTable1.getSelectedRow(), 2));
                    String sinopsis = "" + jTable1.getValueAt(jTable1.getSelectedRow(), 3);

                    Pelicula peliculaAEliminar = new Pelicula(titulo, anyo, puntuacion, sinopsis);

                    conexion.ejecutarDelete(peliculaAEliminar);
            
                    actualizarTabla(); // Actualizamos la tabla

                } catch (Exception a) {
                    a.getLocalizedMessage();
                }

            }
        });
        
        // En este método enviaremos los datos de la pelicula seleccionara al otro Frame, y abriremos la ventana del mismo donde podremos editarlos.
        editar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {

                try {
            
                    // Guardamos los diferentes parámetros de la película.

                    Pelicula peliculaSeleccionada = tablemodel.getValueAt(jTable1.getSelectedRow());
    
                    editarCatalogo editarPelicula = new editarCatalogo(peliculaSeleccionada, conexion, CatalogoCine.this); // Creamos un objeto editarCatalogo con todos los datos necesarios.
                    editarPelicula.setVisible(true); // Hacemos visible la venta
            
                    actualizarTabla();

                } catch (Exception ex) {
                    ex.getLocalizedMessage();
                }
            }
        });
    
    }

